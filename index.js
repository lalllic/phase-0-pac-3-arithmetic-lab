// adds a and b
function add(a, b) {
    return a + b;
}

// subtracts b from a
function subtract(a, b) {
    return a - b;
}

// multiplies a and b
function multiply(a, b) {
    return a * b;
}

// divides b from a
function divide(a, b) {
    return a / b;
}

// adds 1
function increment(a) {
    return a + 1;
}

// subtracts 1
function decrement(a) {
    return a - 1;
}

// turns `n` into an integer
function makeInt(n) {
    return parseInt(n, 10);
}

// turns `n` into a float
function preserveDecimal(n) {
    return parseFloat(n, 10);
}